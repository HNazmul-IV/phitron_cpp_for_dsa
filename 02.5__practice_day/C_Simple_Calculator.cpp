#include <iostream>
#include <string>

using namespace std;

int main()
{
    long long int x, y;
    cin >> x >> y;

    // Printing
    cout << x << " + " << y << " = " << x + y << endl;
    cout << x << " * " << y << " = " << x * y << endl;
    cout << x << " - " << y << " = " << x - y;

    return 0;
}